# By pop method
# By del 
# by remove 

fruits = ['mango','orange','papaya','watermelon','cheeku', 'apple']
print(fruits)

fruits.pop()        # always deletes last entry
print(fruits)

fruits.pop(0)
print(fruits)

del fruits[2]
print(fruits)

fruits.remove('papaya')
print(fruits)
# if same entries there it always deletes first one 