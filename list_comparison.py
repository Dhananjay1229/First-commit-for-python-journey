# == & is
fruits1 = ['mango','orange','papaya']
fruits2 = ['watermelon','cheeku', 'apple','mango']
fruits3 = ['mango','orange','papaya']

print(fruits1 == fruits2)
print(fruits1 == fruits3) # it will compare the values the object holds 
print(fruits1 is fruits3) # it will compare the position the objects had